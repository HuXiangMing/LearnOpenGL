// 5-12CubeMaps_Skybox_Environment_Mapping2 : 此文件包含 "main" 函数。程序执行将在此处开始并结束。

/*
立方体贴图_环境映射_折射
应用：立方体折射天盒的贴图_效果相似一个放大镜

环境映射(Environment Mapping)
我们现在将整个环境映射到了一个纹理对象上了，能利用这个信息的不仅仅只有天空盒。
通过使用环境的立方体贴图，我们可以给物体反射和折射的属性。
这样使用环境立方体贴图的技术叫做环境映射(Environment Mapping)，
其中最流行的两个是反射(Reflection)和折射(Refraction)。

折射
环境映射的另一种形式是折射，它和反射很相似。折射是光线由于传播介质的改变而产生的方向变化。
在常见的类水表面上所产生的现象就是折射，光线不是直直地传播，而是弯曲了一点。
将你的半只胳膊伸进水里，观察出来的就是这种效果。

计算反射向量
我们根据观察方向向量I¯和物体的法向量N¯，来计算反射向量R¯。
我们可以使用GLSL内建的reflect函数来计算这个反射向量。最终的R¯
向量将会作为索引/采样立方体贴图的方向向量，返回环境的颜色值。

折射是通过斯涅尔定律(Snell’s Law)来描述的.

同样，我们有一个观察向量I¯，一个法向量N¯，而这次是折射向量R¯。
可以看到，观察向量的方向轻微弯曲了。弯折后的向量R¯将会用来从立方体贴图中采样。

计算折射向量 refract
折射可以使用GLSL的内建refract函数来轻松实现，它需要一个法向量、一个观察方向和两个材质之间的折射率(Refractive Index)。

折射率决定了材质中光线弯曲的程度，每个材质都有自己的折射率。一些最常见的折射率可以在下表中找到：
材质			折射率
空气			1.00
水			1.33
冰			1.309
玻璃			1.52
钻石			2.42

我们使用这些折射率来计算光传播的两种材质间的比值。
在我们的例子中，光线/视线从空气进入玻璃（如果我们假设箱子是玻璃制的），所以比值为1.001.52=0.658。

通过改变折射率，你可以创建完全不同的视觉效果。编译程序并运行，但结果并不是很有趣，因为我们只使用了一个简单的箱子，
它不太能显示折射的效果，现在看起来只是有点像一个放大镜。
对纳米装使用相同的着色器却能够展现出了我们期待的效果：一个类玻璃的物体。

你可以想象出有了光照、反射、折射和顶点移动的正确组合，你可以创建出非常漂亮的水。
注意，如果要想获得物理上精确的结果，我们还需要在光线离开物体的时候再次折射，
现在我们使用的只是单面折射(Single-side Refraction)，但它对大部分场合都是没问题的。

动态环境贴图
现在我们使用的都是静态图像的组合来作为天空盒，看起来很不错，但它没有在场景中包括可移动的物体。
我们一直都没有注意到这一点，因为我们只使用了一个物体。如果我们有一个镜子一样的物体，周围还有多个物体，
镜子中可见的只有天空盒，看起来就像它是场景中唯一一个物体一样。

通过使用帧缓冲，我们能够为物体的6个不同角度创建出场景的纹理，并在每个渲染迭代中将它们储存到一个立方体贴图中。
之后我们就可以使用这个（动态生成的）立方体贴图来创建出更真实的，包含其它物体的，反射和折射表面了。
这就叫做动态环境映射(Dynamic Environment Mapping)，因为我们动态创建了物体周围的立方体贴图，并将其用作环境贴图。

虽然它看起来很棒，但它有一个很大的缺点：我们需要为使用环境贴图的物体渲染场景6次，这是对程序是非常大的性能开销。
现代的程序通常会尽可能使用天空盒，并在可能的时候使用预编译的立方体贴图，只要它们能产生一点动态环境贴图的效果。
虽然动态环境贴图是一个很棒的技术，但是要想在不降低性能的情况下让它工作还是需要非常多的技巧的。
*/


