/*
深度测试

之前我们在渲染箱子的时候运用了深度缓冲来防止被阻挡的面渲染到其它面的前面。
现在，我们会更深入的讨论这些储存在深度缓冲（Z缓冲zbuffer）中的深度值，以及他们是如何确定一个片段是处于其他片段后方的。

深度缓冲
深度缓冲就像颜色缓冲一样，在每个片段里储存了信息，比那个且和颜色缓冲有一样的宽度和高度。
深度缓冲是由窗口系统自动创建的，它会以16，24，32为float的形式储存它的深度值，但在大部分系统中，深度缓冲的精度都是在24位的。

启用深度测试
当深度测试被启用时，OpenGL会将每一个片段的深度值与深度缓冲的内容进行对比。
如果通过了这个测试，深度缓冲将会更新位新的深度值。如果深度测试失败了，片段会被丢弃。

提前深度测试
现在大部分的GPU都提供一个叫做提前深度测试(Early Depth Testing)的硬件特性。
提前深度测试允许深度测试在片段着色器之前运行。只要我们清楚一个片段永远不会是可见的（它在其他物体之后），我们就能提前丢弃这个片段。

启用与禁用深度测试
深度测试默认是禁用的，所以如果要启用深度测试的话使用glEnable(GL_DEPTH_TEST);
如果你启用了深度缓冲，你还应该在每个渲染迭代之前使用GL_DEPTH_BUFFER_BIT来清除深度缓冲，否则你会仍在使用上一次渲染迭代中的写入的深度值：
glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);

禁用深度缓冲的写入(不在更新深度缓冲)
可以想象，在某些情况下你会需要对所有片段都执行深度测试并丢弃相应的片段，但不希望更新深度缓冲。
使用一个只读的(Read-only)深度缓冲。OpenGL允许我们禁用深度缓冲的写入，只需要设置它的深度掩码(Depth Mask)设置为GL_FALSE就可以了：
glDepthMask(GL_FALSE);注意这只在深度测试被启用的时候才有效果。

深度测试函数
OpenGL允许我们修改深度测试中使用的比较运算符。这允许我们来控制OpenGL什么时候该通过或丢弃一个片段，什么时候去更新深度缓冲。
我们可以调用glDepthFunc函数来设置比较运算符（或者说深度函数(Depth Function)）
glDepthFunc(GL_LESS);这个函数接受下面表格中的比较运算符：
函数			描述
GL_ALWAYS	永远通过深度测试（最后绘制的片段将会总是会渲染在之前绘制片段的上面）
GL_NEVER	永远不通过深度测试
GL_LESS		在片段深度值小于缓冲的深度值时通过测试
GL_EQUAL	在片段深度值等于缓冲区的深度值时通过测试
GL_LEQUAL	在片段深度值小于等于缓冲区的深度值时通过测试
GL_GREATER	在片段深度值大于缓冲区的深度值时通过测试
GL_NOTEQUAL	在片段深度值不等于缓冲区的深度值时通过测试
GL_GEQUAL	在片段深度值大于等于缓冲区的深度值时通过测试

默认情况下使用的深度函数是GL_LESS，它将会丢弃深度值大于等于当前深度缓冲值的所有片段。
*/


