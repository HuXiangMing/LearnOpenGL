/*
混合_丢弃片段

混合(Blend)
OpenGL中，混合(Blending)通常是实现物体透明度(Transparency)的一种技术。
透明就是说一个物体不是纯色的，它的颜色是物体本身的颜色和它背后其它物体的颜色的不同强度结合。
一个有色玻璃窗是一个透明的物体，玻璃有它自己的颜色，但它最终的颜色还包含了玻璃之后所有物体的颜色。
混合(Blend)（不同物体的）多种颜色为一种颜色。所以透明度能让我们看穿物体。

透明度
透明的物体可以是完全透明的（让所有的颜色穿过），或者是半透明的（它让颜色通过，同时也会显示自身的颜色）。
一个物体的透明度是通过它颜色的alpha值来决定的。Alpha颜色值是颜色向量的第四个分量。
当alpha值为0.5时，物体的颜色有50%是来自物体自身的颜色，50%来自背后物体的颜色。

纹理有三个颜色分量：红、绿、蓝。但一些材质会有一个内嵌的alpha通道，对每个纹素(Texel)都包含了一个alpha值。
这个alpha值精确地告诉我们纹理各个部分的透明度。

丢弃片段
有些图片并不需要半透明，只需要根据纹理颜色值，显示一部分，或者不显示一部分，没有中间情况。
我们可以丢弃(Discard)显示纹理中透明部分的片段，不将这些片段存储到颜色缓冲中。

加载透明纹理
要想加载有alpha值的纹理，我们并不需要改很多东西，stb_image在纹理有alpha通道的时候会自动加载，
但我们仍要在纹理生成过程中告诉OpenGL，我们的纹理现在使用alpha通道了
glTexImage2D(GL_TEXTURE_2D, 0, GL_RGBA, width, height, 0, GL_RGBA, GL_UNSIGNED_BYTE, data);

保证你在片段着色器中获取了纹理的全部4个颜色分量，而不仅仅是RGB分量

GLSL的discard命令
一旦被调用，它就会保证片段不会被进一步处理，所以就不会进入颜色缓冲。
有了这个指令，我们就能够在片段着色器中检测一个片段的alpha值是否低于某个阈值，如果是的话，则丢弃这个片段

关于边缘白色边框问题
当采样纹理的边缘的时候，OpenGL会对边缘的值和纹理下一个重复的值进行插值（因为我们将它的环绕方式设置为了GL_REPEAT）。
这通常是没问题的，但是由于我们使用了透明值，纹理图像的顶部将会与底部边缘的纯色值进行插值。
这样的结果是一个半透明的有色边框，你可能会看见它环绕着你的纹理四边形。
要想避免这个，每当你alpha纹理的时候，请将纹理的环绕方式设置为GL_CLAMP_TO_EDGE：
glTexParameteri( GL_TEXTURE_2D, GL_TEXTURE_WRAP_S, GL_CLAMP_TO_EDGE);
glTexParameteri( GL_TEXTURE_2D, GL_TEXTURE_WRAP_T, GL_CLAMP_TO_EDGE);

*/


