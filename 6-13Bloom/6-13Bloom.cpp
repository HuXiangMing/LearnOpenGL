/*
HDR：

应用：增强明亮物体的亮度和辐射效果，从而使场景更加真实和美观。

前言：
OpenGL 中的泛光（Bloom）是一种用于增强场景中明亮物体的效果的后处理技术。
它可以使明亮物体的亮度扩散到周围区域，产生出柔和的辐射效果，从而增强场景的真实感和美感。

泛光通常是通过以下步骤实现的：
1.渲染场景到一个纹理缓冲区（通常称为帧缓冲区）中。

2.提取纹理缓冲区中亮度较高的区域，比如可以通过对场景图像进行高斯模糊来实现。

3.将提取出的高亮区域与原始场景图像进行混合，从而使亮度扩散到周围区域。

4.可以多次重复步骤2和步骤3，以达到更加强烈的泛光效果。

5.在实现泛光时，需要注意一些细节。例如，高斯模糊需要占用一定的计算资源，
因此需要权衡模糊的范围和效果。此外，需要调整混合的参数，以达到最佳的效果和性能。

6.总的来说，泛光是一种非常实用和流行的后处理效果，可以帮助场景更加真实、逼真。
*/




#include <iostream>

#define GLEW_STATIC

#include <GL/glew.h>
#include <GLFW/glfw3.h>
