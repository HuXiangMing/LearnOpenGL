/*
立方体贴图_环境映射_反射
应用：立方体反射天盒的贴图

环境映射(Environment Mapping)
我们现在将整个环境映射到了一个纹理对象上了，能利用这个信息的不仅仅只有天空盒。
通过使用环境的立方体贴图，我们可以给物体反射和折射的属性。
这样使用环境立方体贴图的技术叫做环境映射(Environment Mapping)，
其中最流行的两个是反射(Reflection)和折射(Refraction)。

反射
反射这个属性表现为物体（或物体的一部分）反射它周围环境，即根据观察者的视角，物体的颜色或多或少等于它的环境。
镜子就是一个反射性物体：它会根据观察者的视角反射它周围的环境。

计算反射向量
我们根据观察方向向量I¯和物体的法向量N¯，来计算反射向量R¯。
我们可以使用GLSL内建的reflect函数来计算这个反射向量。最终的R¯
向量将会作为索引/采样立方体贴图的方向向量，返回环境的颜色值。

因为我们使用了法线，你还需要更新一下顶点数据，并更新属性指针。还要记得去设置cameraPos这个uniform。

反射贴图(Reflection Map)
这看起来非常棒，但在现实中大部分的模型都不具有完全反射性。我们可以引入反射贴图(Reflection Map)，来给模型更多的细节。
与漫反射和镜面光贴图一样，反射贴图也是可以采样的纹理图像，它决定这片段的反射性。通过使用反射贴图，我们可以知道模型的哪些部分该以什么强度显示反射。
*/


