/*
陡峭视差贴图：

应用：使用视差贴图，让一张2D图具有深度（高度）效果

前言：
陡峭视差贴图（Disparity Map）和视差贴图（Depth Map）都是计算机视觉中常用的图像处理技术
用于描述图像中不同位置的物体之间的距离关系。


陡峭视差贴图：
陡峭视差贴图则是一种基于视差梯度的计算方法，它是指相邻像素视差值之间的差异。
陡峭视差贴图可以用来提取物体边缘，因为物体边缘处的视差变化会比较大，相邻像素之间的视差差异也会比较大。
陡峭视差贴图一般表示为彩色图像，其中不同颜色表示不同的视差梯度强度，从而能够更直观地表现出物体边缘。


陡峭视差贴图优缺点：

优点：

陡峭视差贴图能够准确地表达物体之间的距离差异，能够提供更加精细的深度信息，适用于一些需要精确深度信息的应用。

陡峭视差贴图的计算速度通常比视差贴图更快，因为它不需要进行归一化和平滑等操作。

缺点：

陡峭视差贴图通常需要进行归一化和平滑等操作，才能更好地表示深度信息。这些操作会增加计算时间和计算复杂度。

陡峭视差贴图容易受到光照和纹理等因素的影响，对于一些复杂场景的深度估计效果不佳。

*/




