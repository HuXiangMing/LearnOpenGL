// 5-02Depth_Test_View : 此文件包含 "main" 函数。程序执行将在此处开始并结束。
 
/*
深度测试可视化

深度值精度和线性深度缓冲
深度缓冲包含了一个介于0.0-1.0之间的深度值，它将会与观察者视角所看见场景中所有物体点的Z值进行比较。
观察空间的Z值可能时投影横截面的近平面（Near）和原平面（Far）之间的任何值。
所以我们需要一种方式来将这些观察空间的Z值变换到【0-1】之间。
有一种方式就是将他们线性变换到【0-1】范围之间。。Fdepth=z−near/far−near

然而，在实际中几乎永远不会使用这样的线性深度缓冲，要想正确的投影性质，需要使用一个非线性的深度方程。
它是与1/Z成正比的。在Z很小是提供很高的精度，而在Z较大时提供较低的精度。
由于非线性方程与1/Z成正比，在1.0-2.0之间的Z值将会占到50%的精度，2.0-100之间的Z值会占有另外的50%。
在Z较大时50-100之间的Z值只会占2%的精度，这正是我们需要的。方程如下
Fdepth=（1/z−1/near）/（1/far−1/near）
这很重要，深度缓冲中的值在屏幕空间中不是线性的（在透视矩阵应用之前在观察空间中是线性的）。
如果我们想要可视化深度缓冲的话，非线性方程的效果很快就会变得很清楚。

深度缓冲的可视化
我们知道在片段着色器中，gl_FragCoord向量的Z值包含了那个特定片段的深度值。如果我们将这个深度值输出为颜色
我们就可以显示场景中所有片段的深度值。FragColor = vec4(vec3(gl_FragCoord.z), 1.0);
因为屏幕空间中的深度值是非线性的，即它在Z值很小的时候有很高度的精度。而Z很大的时候有很低的精度。
而片段的深度会随着距离的增加迅速增加，所以几乎所有的顶点的深度值都是接近于1.0的。
如果我们靠近物体，可能颜色会渐渐变暗，因为他们的Z值在逐渐减小。

将屏幕空间深度值的非线性，转为线性
要实现片段非线性的深度值变换为线性，我们需要仅仅翻转深度值的投影变换。
也就意味着我们需要首先将深度值从【0-1】范围重新变换到【-1，1】范围的标准化设备坐标（裁剪空间）
接下来我们需要像投影矩阵那样翻转这个非线性方程，并将这个反转的方程应用到最终的深度值上。最终就是一个线性的深度值了。
首先将深度值变换为NDC(从【0-1】范围重新变换到【-1，1】范围)
float z = depth * 2.0 - 1.0;
接下来使用获取到的z值，应用逆变换来获取线性的深度值：
float linearDepth = (2.0 * near * far) / (far + near - z * (far - near));(方程是用投影矩阵推导得出的)

深度冲突
一个很常见的视觉错误会在两个平面或者三角形非常紧密地平行排列在一起时会发生，深度缓冲没有足够的精度来决定两个形状哪个在前面。
结果就是这两个形状不断地在切换前后顺序，这会导致很奇怪的花纹。这个现象叫做深度冲突(Z-fighting)，因为它看起来像是这两个形状在争夺(Fight)谁该处于顶端。

场景中，有几个地方的深度冲突还是非常明显的。
箱子被放置在地板的同一高度上，这也就意味着箱子的底面和地板是共面的(Coplanar)。
这两个面的深度值都是一样的，所以深度测试没有办法决定应该显示哪一个。

深度冲突是深度缓冲的一个常见问题，当物体在远处时效果会更明显（因为深度缓冲在z值比较大的时候有着更小的精度）。

防止深度冲突
第一个也是最重要的技巧是永远不要把多个物体摆得太靠近，以至于它们的一些三角形会重叠。
通过在两个物体之间设置一个用户无法注意到的偏移值，你可以完全避免这两个物体之间的深度冲突。
在箱子和地板的例子中，我们可以将箱子沿着正y轴稍微移动一点。
箱子位置的这点微小改变将不太可能被注意到，但它能够完全减少深度冲突的发生。
然而，这需要对每个物体都手动调整，并且需要进行彻底的测试来保证场景中没有物体会产生深度冲突。

第二个技巧是尽可能将近平面设置远一些。
在前面我们提到了精度在靠近近平面时是非常高的，所以如果我们将近平面远离观察者，我们将会对整个平截头体（是一个头部被截取的棱锥，代表了一个可是区域的投影）有着更大的精度。
然而，将近平面设置太远将会导致近处的物体被裁剪掉，所以这通常需要实验和微调来决定最适合你的场景的近平面距离。


另外一个很好的技巧是牺牲一些性能，使用更高精度的深度缓冲。
大部分深度缓冲的精度都是24位的，但现在大部分的显卡都支持32位的深度缓冲，这将会极大地提高精度。
所以，牺牲掉一些性能，你就能获得更高精度的深度测试，减少深度冲突。
*/


