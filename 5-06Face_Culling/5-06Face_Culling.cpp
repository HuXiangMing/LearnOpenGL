/*
面剔除

封闭的3D立方体最多能看到3个面
尝试在脑子中想象一个3D立方体，数数你从任意方向最多能同时看到几个面。
你可以从任意位置和任意方向看向这个立方体，但你永远不能看到3个以上的面。

如果我们能够以某种方式丢弃这几个看不见的面，我们能省下超过50%的片段着色器执行数！
因为从特定角度来看的话只能看见2个甚至是1个面。在这种情况下，我们就能省下超过50%了。

一个面片有两侧，正面和背面
这是一个很好的主意，但我们仍有一个问题需要解决：我们如何知道一个物体的某一个面不能从观察者视角看到呢？
如果我们想象任何一个闭合形状，它的每一个面都有两侧，每一侧要么面向用户，要么背对用户。如果我们能够只绘制面向观察者的面呢？

正面剔除
这正是面剔除(Face Culling)所做的。OpenGL能够检查所有面向(Front Facing)观察者的面，并渲染它们，而丢弃那些背向(Back Facing)的面，节省我们很多的片段着色器调用（它们的开销很大！）。

但我们仍要告诉OpenGL哪些面是正向面(Front Face)，哪些面是背向面(Back Face)。OpenGL使用了一个很聪明的技巧，分析顶点数据的环绕顺序(Winding Order)。

环绕顺序
当我们定义一组三角形顶点时，我们会以特定的环绕顺序来定义它们，可能是顺时针(Clockwise)的，也可能是逆时针(Counter-clockwise)的。
每个三角形由3个顶点所组成，我们会从三角形中间来看，为这3个顶点设定一个环绕顺序。
所以顶点数据的排序很重要，他决定了正面与背面

逆时针顶点所定义的三角形将会被处理为正向三角形
每组组成三角形图元的三个顶点就包含了一个环绕顺序。OpenGL在渲染图元的时候将使用这个信息来决定一个三角形是一个正向三角形还是背向三角形。
默认情况下，逆时针顶点所定义的三角形将会被处理为正向三角形。

立方体的面剔除
因为立方体另一个面的三角形顶点是以相反的环绕顺序渲染的（假如正面为（1，2，3）逆时针渲染为正面，
那么对面的三角形则是背面（从正面看的话就是1，3，2顺时针渲染，但你面朝背面那个三角形时，你会发现还是逆时针））
虽然背面三角形时以逆时针定义的，但它现在以顺时针渲染了，这正是我们想要剔除的面（因为它不需要被看见）

OpenGL能够丢弃那些渲染为背面的三角形图元，它默认是禁用的。
但在使用时要记得顶点的环绕顺序，确认在每个三角形中它们都是以逆时针定义的，这是一个很好的习惯。
要想启用面剔除，我们只需要启用OpenGL的GL_CULL_FACE选项，glEnable(GL_CULL_FACE);

这一句代码之后，所有背向面都将被丢弃（尝试飞进立方体内部，看看所有的内面是不是都被丢弃了）。
目前我们在渲染片段的时候能够节省50%以上的性能，但注意这只对像立方体这样的封闭形状有效。
如上一节课的草，我们必须要再次禁用面剔除，因为它们的正向面和背向面都应该是可见的。

OpenGL允许我们改变需要剔除的面的类型。如果我们只想剔除正向面而不是背向面会怎么样？我们可以调用glCullFace来定义这一行为：
glCullFace(GL_FRONT);
GL_BACK：只剔除背向面。
GL_FRONT：只剔除正向面。
GL_FRONT_AND_BACK：剔除正向面和背向面。

除了需要剔除的面之外，我们也可以通过调用glFrontFace，告诉OpenGL我们希望将顺时针的面（而不是逆时针的面）定义为正向面：
默认值是GL_CCW，它代表的是逆时针的环绕顺序，另一个选项是GL_CW，它（显然）代表的是顺时针顺序。
*/



