/*
SSAO（Screen Space Ambient Occlusion）屏幕空间环境光遮蔽


应用：对延迟渲染管线的优化__光体积


前言：
在现实中，光线会以任意方向散射，它的强度是会一直改变的，所以间接被照到的那部分场景也应该有变化的强度，而不是一成不变的环境光。

其中一种间接光照的模拟叫做屏幕空间环境光遮蔽(Screen-Space Ambient Occlusion, SSAO)的技术。
SSAO是一种用于模拟环境光遮蔽效果的渲染技术，通过在屏幕空间内对场景中的几何信息进行采样和分析，
生成一种模拟遮蔽的效果。它主要用于增强图形渲染的真实感和细节。

SSAO的基本原理是在屏幕空间内对每个像素的深度信息进行采样，并通过随机采样周围的像素来计算每个像素的遮蔽程度。这种遮蔽程度可以通过计算周围像素深度与当前像素深度之间的距离来获得，距离越近的像素遮蔽程度越高。

SSAO通常分为以下几个步骤：

1.深度提取：将场景中每个像素的深度信息提取到一个深度缓冲区中，这通常是在延迟着色器中完成的。

2.随机采样：在屏幕空间内随机采样一组像素，并计算每个像素与当前像素之间的距离。

3.遮蔽计算：根据距离和一些参数（如采样半径和遮蔽强度）计算每个像素的遮蔽程度，并将其存储到一个纹理中。

4.合成：将遮蔽纹理与场景中的颜色纹理进行合成，生成最终的渲染图像。

SSAO的优点是它可以增强图形渲染的真实感和细节，特别是在室内和暗场景中。
SSAO的缺点是它可能会产生一些不自然的阴影效果，并且对性能有一定的影响。
为了解决这些问题，可以使用一些改进的技术，如HBAO（Horizon-Based Ambient Occlusion）和SSDO（Screen Space Directional Occlusion）等。

*/




#include <iostream>

#define GLEW_STATIC

